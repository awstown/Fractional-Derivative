clear all; close all;

%% Solve initial value problem y'=1-t+4*y, y(0)=1 by Euler's method

% Analytical Solution


% Initial value
t=0; y=1;
% Step size, number of steps
h=0.01; N=100;

for j=1:N
    y=y + h*(1-t+4*y);
    t=t+h;
end

